    private void searchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchMouseClicked

    String coachId = searchField.getText().trim(); 

if (coachId.isEmpty() || !coachId.matches("\\d+")) {
    JOptionPane.showMessageDialog(this, "Please enter a valid numeric Coach ID.");
    return;
}

try {
    dbConnector dbc = new dbConnector(); 

    String query = "SELECT * FROM tbl_coach WHERE coach_id = ?";
    PreparedStatement ps = dbc.connect.prepareStatement(query);
    ps.setInt(1, Integer.parseInt(coachId));
    ResultSet rs = ps.executeQuery();

    DefaultTableModel model = (DefaultTableModel) coachTable.getModel(); 
    model.setRowCount(0); 

    while (rs.next()) {
        Object[] row = {
            rs.getInt("coach_id"),
            rs.getString("full_name"),
            rs.getString("specialty"),
            rs.getInt("experience_years"),
            rs.getString("contact_number"),
            rs.getString("status")
        };
        model.addRow(row);
    }

    if (model.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "No coach found with ID: " + coachId);
    }

    rs.close();
    ps.close();

} catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
}



    }//GEN-LAST:event_searchMouseClicked

    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
       int selectedRow = coachTable.getSelectedRow(); 

if (selectedRow == -1) {
    JOptionPane.showMessageDialog(this, "Please select a coach to delete.");
    return;
}

int coachId = (int) coachTable.getValueAt(selectedRow, 0);
int confirm = JOptionPane.showConfirmDialog(
    this,
    "Are you sure you want to delete coach with ID: " + coachId + "?",
    "Confirm Delete",
    JOptionPane.YES_NO_OPTION
);

if (confirm == JOptionPane.YES_OPTION) {
    try {
        dbConnector dbc = new dbConnector();

        String deleteQuery = "DELETE FROM tbl_coach WHERE coach_id = ?";
        PreparedStatement ps = dbc.connect.prepareStatement(deleteQuery);
        ps.setInt(1, coachId);
        int rowsAffected = ps.executeUpdate();
        ps.close();

        if (rowsAffected > 0) {

            
            Session sess = Session.getInstance();
            int currentUserId = sess.getUserId();

            String logAction = "Deleted Coach with ID: " + coachId;
            String logQuery = "INSERT INTO logs (user_id, action, date) VALUES (?, ?, NOW())";
            PreparedStatement logPst = dbc.connect.prepareStatement(logQuery);
            logPst.setInt(1, currentUserId);
            logPst.setString(2, logAction);
            logPst.executeUpdate();
            logPst.close();

            JOptionPane.showMessageDialog(this, "Coach ID " + coachId + " deleted successfully.");
            ((DefaultTableModel) coachTable.getModel()).removeRow(selectedRow); 
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete coach.");
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage());
        ex.printStackTrace();
    }
}



    }//GEN-LAST:event_deleteMouseClicked

    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        int rowIndex = coachTable.getSelectedRow();

if (rowIndex < 0) {
    JOptionPane.showMessageDialog(null, "Please select a coach!");
} else {
    try {
        dbConnector dbc = new dbConnector();
        TableModel tbl = coachTable.getModel();
        ResultSet rs = dbc.getData(
            "SELECT * FROM tbl_coach WHERE coach_id = '"
            + tbl.getValueAt(rowIndex, 0)
            + "'"
        );

        if (rs.next()) {
            createCoach ccf = new createCoach();
            ccf.coachid.setText("" + rs.getInt("coach_id"));
            ccf.fn.setText(rs.getString("full_name"));
            ccf.sty.setText(rs.getString("specialty"));
            ccf.ey.setText("" + rs.getInt("experience_years"));
            ccf.cn.setText(rs.getString("contact_number"));
            ccf.stat.setSelectedItem(rs.getString("status"));
            ccf.add.setEnabled(false);
            ccf.update.setEnabled(true);
            ccf.setVisible(true);
            this.dispose();
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Database Error: " + ex.getMessage());
        ex.printStackTrace();
    }
}

    }//GEN-LAST:event_editMouseClicked

