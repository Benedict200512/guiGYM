    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed

        
     if (fn.getText().isEmpty()   
     || sty.getText().isEmpty() 
     || ey.getText().isEmpty()   
     || cn.getText().isEmpty()   
     || stat.getSelectedItem() == null) { 
        JOptionPane.showMessageDialog(null, "All fields are required!");
        return;
    }
    
    if (!ey.getText().matches("\\d+")) {
        JOptionPane.showMessageDialog(null,    "Experience must be a whole number of years.");
        ey.setText("");
        return;
    }
    if (!cn.getText().matches("\\d+")) {
        JOptionPane.showMessageDialog(null, "Contact number must contain only digits.");
        cn.setText("");
        return;
    }

    
    String fullName        = fn.getText().trim();
    String specialty       = sty.getText().trim();
    int    experienceYears = Integer.parseInt(ey.getText().trim());
    String contactNumber   = cn.getText().trim();
    String status          = stat.getSelectedItem().toString();

    try {
        dbConnector dbc = new dbConnector();
        String sql = ""
          + "INSERT INTO tbl_coach "
          + "(full_name, specialty, experience_years, contact_number, status) "
          + "VALUES (?, ?, ?, ?, ?)";

        PreparedStatement pst = dbc.connect
          .prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
        pst.setString(1, fullName);
        pst.setString(2, specialty);
        pst.setInt(3, experienceYears);
        pst.setString(4, contactNumber);
        pst.setString(5, status);

        int rowsInserted = pst.executeUpdate();

        if (rowsInserted > 0) {
            
            ResultSet rs = pst.getGeneratedKeys();
            int coachId = -1;
            if (rs.next()) {
                coachId = rs.getInt(1);
            }

            
            Session sess = Session.getInstance();
            int userId = sess.getUserId(); 
            if (userId > 0) {
                String logAction = "Created Coach Record with ID No. " + coachId;
                String logSql    = "INSERT INTO logs (user_id, action, date) VALUES (?, ?, NOW())";
                PreparedStatement logPst = dbc.connect.prepareStatement(logSql);
                logPst.setInt(1, userId);
                logPst.setString(2, logAction);
                logPst.executeUpdate();
                logPst.close();
            } else {
                JOptionPane.showMessageDialog(null, "Invalid session user ID. Logging skipped.");
            }

            JOptionPane.showMessageDialog(null, "Coach added successfully!");
            coachForm cf = new coachForm();
            cf.setVisible(true);
            this.dispose();
            
            fn.setText("");
            sty.setText("");
            ey.setText("");
            cn.setText("");
            stat.setSelectedIndex(0);

        } else {
            JOptionPane.showMessageDialog(null, "Failed to add coach.");
        }
        pst.close();

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Database Error: " + ex.getMessage());
        ex.printStackTrace();
    }

    }//GEN-LAST:event_addActionPerformed

